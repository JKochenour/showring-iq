import {
  Document,
  Page,
  StyleSheet,
  Text,
  View,
} from "@react-pdf/renderer";
import type { ShowResultsData } from "@/lib/load-show-results";

const styles = StyleSheet.create({
  page: { padding: 32, fontSize: 10, fontFamily: "Helvetica" },
  showTitle: { fontSize: 16, fontWeight: 700, marginBottom: 2 },
  showMeta: { fontSize: 9, color: "#52525b", marginBottom: 16 },
  classHeader: {
    fontSize: 12,
    fontWeight: 700,
    marginTop: 16,
    marginBottom: 2,
    borderBottom: "1pt solid #d4d4d8",
    paddingBottom: 4,
  },
  classMeta: { fontSize: 9, color: "#52525b", marginBottom: 6 },
  row: {
    flexDirection: "row",
    borderBottom: "0.5pt solid #e4e4e7",
    paddingVertical: 3,
  },
  headerRow: {
    flexDirection: "row",
    borderBottom: "1pt solid #18181b",
    paddingBottom: 3,
    marginTop: 4,
    fontWeight: 700,
  },
  colPlacing: { width: "10%" },
  colBack: { width: "12%" },
  colRider: { width: "30%" },
  colHorse: { width: "28%" },
  colScore: { width: "20%", textAlign: "right" },
  scratched: { color: "#a1a1aa" },
  footer: {
    position: "absolute",
    bottom: 24,
    left: 32,
    right: 32,
    fontSize: 8,
    color: "#a1a1aa",
    textAlign: "center",
  },
});

function formatDateRange(start: string, end: string) {
  if (!start) return "";
  const fmt = (d: string) =>
    new Date(`${d}T00:00:00`).toLocaleDateString("en-US", {
      month: "short",
      day: "numeric",
      year: "numeric",
    });
  return start === end ? fmt(start) : `${fmt(start)} - ${fmt(end)}`;
}

export function ResultsDocument({ data }: { data: ShowResultsData }) {
  return (
    <Document>
      <Page size="LETTER" style={styles.page}>
        <Text style={styles.showTitle}>{data.showName || "Show Results"}</Text>
        <Text style={styles.showMeta}>
          {formatDateRange(data.startDate, data.endDate)}
          {data.showNumber && `  ·  Show #${data.showNumber}`}
        </Text>

        {data.classes.length === 0 && (
          <Text>No official classes yet.</Text>
        )}

        {data.classes.map((cls) => (
          <View key={cls.classId} wrap={false}>
            <Text style={styles.classHeader}>
              Class {cls.classNumber} — {cls.name}
            </Text>
            <Text style={styles.classMeta}>
              {[
                cls.discipline,
                cls.patternNumber ? `Pattern ${cls.patternNumber}` : null,
              ]
                .filter(Boolean)
                .join(" · ")}
            </Text>

            <View style={styles.headerRow}>
              <Text style={styles.colPlacing}>Place</Text>
              <Text style={styles.colBack}>Back #</Text>
              <Text style={styles.colRider}>Rider</Text>
              <Text style={styles.colHorse}>Horse</Text>
              <Text style={styles.colScore}>Score</Text>
            </View>
            {cls.rows.map((row, i) => (
              <View
                key={i}
                style={[styles.row, row.scratched ? styles.scratched : {}]}
              >
                <Text style={styles.colPlacing}>
                  {row.placing ? `${row.placing}${row.tieStatus === "tied" ? "*" : ""}` : "—"}
                </Text>
                <Text style={styles.colBack}>
                  {row.backNumber ? `#${row.backNumber}` : "—"}
                </Text>
                <Text style={styles.colRider}>{row.riderName}</Text>
                <Text style={styles.colHorse}>{row.horseName}</Text>
                <Text style={styles.colScore}>{row.scoreLabel}</Text>
              </View>
            ))}
          </View>
        ))}

        <Text style={styles.footer} fixed>
          Validation assistance based on configured rule package. Final
          responsibility remains with show management and the applicable
          association. Generated by ShowRing IQ.
        </Text>
      </Page>
    </Document>
  );
}
